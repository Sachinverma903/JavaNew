package org.example;

public class Loops {
    public static void main(String[] args) {
        //=========while loop======================
        int num = 1;
        while(num<=10){
            System.out.print(num+" ");
            num++;
        }
        System.out.println();
        //=============for loop=================================
       for(int i= 1;i<=10;i=i+1){
           System.out.print(i+" ");
       }
    }
}
